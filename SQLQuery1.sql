USE QuanLyQuanCafe
GO


CREATE TABLE TABLECOFFEE
(
	IDTABLE INT PRIMARY KEY,
	NAME NVARCHAR(100) NOT NULL,
	STATUS NVARCHAR(100) NOT NULL 
)

CREATE TABLE COFFEETYPE 
(
	IDTYPE INT PRIMARY KEY,
	NAME NVARCHAR(100) NOT NULL,
	NSX DATE,
	ORIGIN NVARCHAR(100) 
)

CREATE TABLE MENU 
(
	IDMENU INT PRIMARY KEY,
	NAME NVARCHAR(100) NOT NULL,
	IDTYPE INT NOT NULL, 
	PRICE FLOAT NOT NULL DEFAULT 0,
	FOREIGN KEY (IDTYPE) REFERENCES COFFEETYPE(IDTYPE),
	AVATARMENU NVARCHAR(100)
)

CREATE TABLE BILL
(
	IDBILL INT PRIMARY KEY,
	IDTABLE INT NOT NULL, 
	STATUS INT 
	FOREIGN KEY (IDTABLE) REFERENCES TABLECOFFEE(IDTABLE) 
)

CREATE TABLE BILLINFO
(
	IDINFO INT PRIMARY KEY,
	IDBILL INT NOT NULL, 
	IDEMPLOYEE INT,
	IDMENU INT NOT NULL, 
	COUNT FLOAT NOT NULL DEFAULT 0, 
	FOREIGN KEY (IDBILL) REFERENCES BILL(IDBILL),
	FOREIGN KEY (IDMENU) REFERENCES MENU(IDMENU),
	FOREIGN KEY (IDEMPLOYEE) REFERENCES EMPLOYEE(IDEMPLOYEE)
)

CREATE TABLE INVENTORY
(
    IDINVENTORY INT PRIMARY KEY,
    IDCOFFEE INT NOT NULL, 
    QUANTITY FLOAT NOT NULL,
	DATE_RECEIVED DATE,
	DATE_EXPIRED DATE,
    UNIT NVARCHAR(50) NOT NULL,
	FOREIGN KEY (IDCOFFEE) REFERENCES COFFEETYPE(IDTYPE) 
)

CREATE TABLE ACCOUNT 
(
	USERNAME NVARCHAR(100) PRIMARY KEY,
	DISPLAYNAME NVARCHAR(100),
	PASSWORD NVARCHAR(100) NOT NULL
)

CREATE TABLE DISCOUNT 
(
	IDDIS INT PRIMARY KEY,
	NAME NVARCHAR(100) NOT NULL,
	DISCOUNT_PERCENTAGE FLOAT NOT NULL, 
	DATE_START DATE,
	DATE_FINISH DATE,
	STATUS NVARCHAR(100)
)

CREATE TABLE DISCOUNTMENU
(
	IDDIS_MENU INT PRIMARY KEY,
	IDDISCOUNT INT NOT NULL, 
	IDMENU INT NOT NULL, 
	FOREIGN KEY (IDDISCOUNT) REFERENCES DISCOUNT(IDDIS) ,
	FOREIGN KEY (IDMENU) REFERENCES MENU(IDMENU) ,
	STATUS NVARCHAR(100)
)

CREATE TABLE EMPLOYEE
(
	IDEMPLOYEE INT PRIMARY KEY,
	NAME NVARCHAR(100),
	POSITION NVARCHAR(100),
	SALARY FLOAT ,
	DATE_HIRE DATE,
	AVATAREMPLOYEE NVARCHAR(100)
)

